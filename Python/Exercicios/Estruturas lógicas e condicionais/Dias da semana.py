dia = int(input('Escreva um número entre 1 e 7: '))

if dia == 1:
    print('Domingo')
elif dia == 2:
    print('Segunda-feira')
elif dia == 3:
    print('Terça-feira')
elif dia == 4:
    print('Quarta-feira')
elif dia == 5:
    print('Quinta-feira')
elif dia == 6:
    print('Sexta-cheira')
elif dia == 7:
    print('Sabádo')
else:
    print('Dia inválido')
